function buildTurnInput(payload, includeHistory) {
  const parts = []
  if (includeHistory && Array.isArray(payload.history) && payload.history.length) {
    const history = payload.history
      .slice(-20)
      .map(m => `${m.role === 'assistant' ? 'Eros' : 'Usuario'}: ${String(m.content || '').slice(0, 4000)}`)
      .join('\n')
    parts.push(`Historial reciente de la conversacion en Clarin:\n${history}`)
  }
  parts.push(`Mensaje del usuario:\n${payload.message}`)
  return parts.join('\n\n')
}

function extractAssistantText(turn) {
  const items = Array.isArray(turn?.items) ? turn.items : []
  const final = [...items].reverse().find(item => item.type === 'agentMessage' && item.phase === 'final_answer' && item.text)
  if (final) return final.text.trim()
  const any = [...items].reverse().find(item => item.type === 'agentMessage' && item.text)
  if (any) return any.text.trim()
  const deltas = Array.isArray(turn?.deltas) ? turn.deltas : []
  return deltas.join('').trim()
}

function extractMcpTools(turn) {
  const items = Array.isArray(turn?.items) ? turn.items : []
  return items
    .filter(item => item.type === 'mcpToolCall')
    .map(item => ({
      name: item.tool,
      status: item.status,
      account_id: getAccountID(item.arguments),
      server: item.server
    }))
}

function extractErosFileExports(turn) {
  const items = Array.isArray(turn?.items) ? turn.items : []
  const out = []
  for (const item of items) {
    if (item?.type !== 'mcpToolCall') continue
    if (!['prepare_file_export', 'render_file_export'].includes(item.tool)) continue
    for (const candidate of findJSONObjects(item)) {
      const hint = sanitizeFileExportHint(candidate)
      if (hint) out.push(hint)
    }
  }
  const seen = new Set()
  return out.filter(hint => {
    const key = `${hint.format || ''}|${hint.filename || ''}|${hint.title || ''}`
    if (seen.has(key)) return false
    seen.add(key)
    return true
  }).slice(0, 3)
}

function findJSONObjects(value, depth = 0) {
  if (depth > 6 || value == null) return []
  if (typeof value === 'string') {
    const text = value.trim()
    if (!text || text.length > 20000) return []
    try {
      return findJSONObjects(JSON.parse(text), depth + 1)
    } catch {
      return []
    }
  }
  if (Array.isArray(value)) return value.flatMap(item => findJSONObjects(item, depth + 1))
  if (typeof value === 'object') {
    const found = []
    if (value.eros_file_export === true || value.file_export === true || value.download_delivery === 'clarin_chat_attachment') {
      found.push(value)
    }
    for (const child of Object.values(value)) {
      found.push(...findJSONObjects(child, depth + 1))
    }
    return found
  }
  return []
}

function sanitizeFileExportHint(value) {
  if (!value || typeof value !== 'object') return null
  const format = normalizeFileFormat(value.format || value.file_format || value.type)
  if (!format) return null
  return {
    format,
    filename: safeShortText(value.filename || value.file_name || ''),
    title: safeShortText(value.title || value.name || '')
  }
}

function normalizeFileFormat(value) {
  const text = String(value || '').trim().toLowerCase().replace(/^\./, '')
  if (['txt', 'text', 'texto'].includes(text)) return 'txt'
  if (text === 'csv') return 'csv'
  if (['xlsx', 'excel'].includes(text)) return 'xlsx'
  if (['docx', 'doc', 'word'].includes(text)) return 'docx'
  if (['pptx', 'ppt', 'powerpoint'].includes(text)) return 'pptx'
  return ''
}

function safeShortText(value) {
  return String(value || '').replace(/[^\w .-]+/g, '_').trim().slice(0, 120)
}

function getAccountID(args) {
  if (typeof args === 'string') {
    try {
      args = JSON.parse(args)
    } catch {
      return ''
    }
  }
  if (!args || typeof args !== 'object') return ''
  const value = args.account_id || args.accountId
  return typeof value === 'string' ? value : ''
}

function summarizeTurnForLog(turn) {
  const items = Array.isArray(turn?.items) ? turn.items : []
  const counts = new Map()
  for (const item of items) {
    const type = item && typeof item.type === 'string' ? item.type : 'unknown'
    counts.set(type, (counts.get(type) || 0) + 1)
  }
  const itemTypes = [...counts.entries()].map(([type, count]) => `${type}:${count}`).join(',')
  return [
    `status=${turn?.status || 'unknown'}`,
    `itemsView=${turn?.itemsView || 'unknown'}`,
    `itemCount=${items.length}`,
    `itemTypes=${itemTypes || 'none'}`,
    `error=${summarizeTurnError(turn)}`
  ].join(' ')
}

function summarizeTurnError(turn) {
  const error = turn?.error || {}
  const code = error.codexErrorInfo ? JSON.stringify(error.codexErrorInfo) : ''
  const message = error.message ? redactError(String(error.message)).slice(0, 240) : ''
  return [code, message].filter(Boolean).join(': ') || 'none'
}

const app = new CodexAppServer()

async function readJSON(req) {
  const chunks = []
  let size = 0
  for await (const chunk of req) {
    size += chunk.length
    if (size > 1024 * 1024) throw new Error('request body too large')
    chunks.push(chunk)
  }
  const raw = Buffer.concat(chunks).toString('utf8')
  return raw ? JSON.parse(raw) : {}
}

function sendJSON(res, status, body) {
  const payload = JSON.stringify(body)
  res.writeHead(status, {
    'content-type': 'application/json; charset=utf-8',
    'content-length': Buffer.byteLength(payload)
  })
  res.end(payload)
}

async function health() {
  const authFileExists = await exists(path.join(CODEX_HOME, 'auth.json')) || await exists(AUTH_FILE)
  const authTokens = await readCodexAuthTokens()
  const status = {
    ok: false,
    bridge: 'clarin-codex-bridge',
    codex_running: Boolean(app.proc && app.proc.exitCode === null),
    codex_initialized: app.initialized,
    codex_auth_file_configured: Boolean(AUTH_FILE),
    codex_auth_file_exists: authFileExists,
    codex_auth_token_valid: tokenIsValid(authTokens),
    codex_auth_token_expires_at: authTokens?.expiresAt || null,
    codex_access_token_configured: Boolean(ACCESS_TOKEN),
    mcp_base_url_configured: Boolean(MCP_BASE_URL),
    mcp_access_token_configured: Boolean(MCP_TOKEN),
    mcp_server: MCP_SERVER_NAME,
    codex_model: CODEX_MODEL || null,
    reasoning_effort: CODEX_REASONING_EFFORT || null,
    mcp_tools_count: 0,
    mcp_auth_status: null,
    last_stderr: app.stderrTail.slice(-3)
  }
  try {
    const auth = await app.authStatus()
    status.codex_auth_method = auth.authMethod || null
    status.codex_requires_openai_auth = Boolean(auth.requiresOpenaiAuth)
    status.codex_authenticated = Boolean(!status.codex_requires_openai_auth || status.codex_auth_token_valid)
    const mcp = await app.mcpStatus()
    const clarin = (mcp.data || []).find(s => s.name === MCP_SERVER_NAME)
    if (clarin) {
      status.mcp_auth_status = clarin.authStatus || null
      status.mcp_tools_count = clarin.tools ? Object.keys(clarin.tools).length : 0
    }
    status.ok = Boolean(app.initialized && status.codex_authenticated && status.mcp_tools_count > 0)
  } catch (err) {
    status.error = redactError(err)
  }
  return status
}

const server = createServer(async (req, res) => {
  try {
    if (req.url === '/health' && req.method === 'GET') {
      if (!hasBridgeAccess(req)) return sendJSON(res, 401, { success: false, error: 'unauthorized' })
      const body = await health()
      return sendJSON(res, body.ok ? 200 : 503, { success: body.ok, ...body })
    }
    if (req.url === '/chat' && req.method === 'POST') {
      if (!hasBridgeAccess(req)) return sendJSON(res, 401, { success: false, error: 'unauthorized' })
      const body = await readJSON(req)
      if (!body.account_id || !body.user_id || !body.conversation_id || !body.message) {
        return sendJSON(res, 400, { success: false, error: 'missing required Eros bridge fields' })
      }
      const result = await app.chat(body)
      return sendJSON(res, 200, result)
    }
    return sendJSON(res, 404, { success: false, error: 'not_found' })
  } catch (err) {
    return sendJSON(res, 500, { success: false, error: redactError(err) })
  }
})

server.listen(PORT, '0.0.0.0', () => {
  console.log(`clarin-codex-bridge listening on ${PORT}`)
})

process.on('SIGTERM', () => {
  if (app.proc) app.proc.kill('SIGTERM')
  server.close(() => process.exit(0))
})
